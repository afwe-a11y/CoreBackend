package com.tenghe.corebackend.kronos.model.enums;

/**
 * 点位数据类型枚举。
 * 定义模型点位支持的数据类型。
 */
public enum PointDataTypeEnum {
  /**
   * 整型
   */
  INT,
  /**
   * 单精度浮点
   */
  FLOAT,
  /**
   * 双精度浮点
   */
  DOUBLE,
  /**
   * 字符串
   */
  STRING,
  /**
   * 枚举
   */
  ENUM,
  /**
   * 布尔
   */
  BOOL,
  /**
   * 日期时间
   */
  DATETIME
}
